package MVC_Datos;

public class Permiso {
    private String empleado_codigo;
    private int modulo_codigo;
    private String estado;

    public String getEmpleado_codigo() {
        return empleado_codigo;
    }

    public void setEmpleado_codigo(String empleado_codigo) {
        this.empleado_codigo = empleado_codigo;
    }

    public int getModulo_codigo() {
        return modulo_codigo;
    }

    public void setModulo_codigo(int modulo_codigo) {
        this.modulo_codigo = modulo_codigo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
    
    
}
